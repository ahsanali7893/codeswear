"use client";
import { useState, useEffect, Component } from "react";
import React from "react";
import "./globals.css";
import Footer from "components/Footer";

import { Inter } from "next/font/google";
import Navbar from "components/Navbar";

const inter = Inter({ subsets: ["latin"] });

export default function RootLayout({ children }) {
  const [cart, setCart] = useState({});
  const [subtotal, setsubtotal] = useState(0);
  useEffect(() => {
    console.log("Hey Iam a useEffect from layout.js");
    try {
      if (localStorage.getItem("cart")) {
        setCart(JSON.parse(localStorage.getItem("cart")));
      }
    } catch (error) {
      console.log(error);
      localStorage.clear();
    }
  }, []);

  const saveCart = (myCart) => {
    localStorage.setItem("cart", myCart);
    let subt = 0;
    let keys = Object.keys(myCart);
    for (let i = 0; i < keys.length; i++) {
      subt += myCart[keys[i]].price * myCart[keys[i]].qty;
    }
    setsubtotal(subt);
  };

  const addToCart = (itemCode, _qty, name, price, size, varriant) => {
    let newCart = cart;
    if (itemCode in cart) {
      newCart[itemCode].qty = cart[itemCode].qty + 1;
    } else {
      newCart[itemCode] = { qty: 1, price, name, size, varriant };
    }
    setCart(newCart);
    saveCart(newCart);
  } ;

  const clearCart = () => {
    setCart({});
    saveCart({});
  };

  const removeFromCart = (itemCode, qty, _name, _price, _size, _varriant) => {
    let newCart = cart;
    if (itemCode in cart) {
      newCart[itemCode].qty = cart[itemCode].qty - qty;
    }
    if (newCart[itemCode]["qty"] <= 0) {
      delete newCart[itemCode];
    }
    setCart(newCart);
    saveCart(newCart);
  };

  const metadata = {
    title: "CodeSwear.com - Wear The Code",
    description: "Generated by create next app",
  };

  return (
    <html lang="en">
      <body className={inter.className}>
        <Navbar
          cart={cart}
          addToCart={addToCart}
          removeFromCart={removeFromCart}
          clearCart={clearCart}
          subtotal={subtotal}
        />
        <tshirts />
        {children}
        <Footer />
      </body>
    </html>
  );
}
